    private void ModifierQuantiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierQuantiteActionPerformed

        PanierDAO pDAO = new PanierDAO();
        try {
            String nom = model.getValueAt(jTable1.getSelectedRow(), 0).toString();
           
            float qte = Float.parseFloat(tfQuantite.getText());
            pDAO.modifierQuantite(nom, qte);

            JOptionPane.showMessageDialog(null, "quantité modifié avec succés");
            tfQuantite.setText(null);

            PanierModel panier = new PanierModel();
            jTable1.setModel(panier);
            panier.fireTableDataChanged();
            model = (AbstractTableModel) jTable1.getModel();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, " Veuillez selectionez un article ");

        }

    }//GEN-LAST:event_ModifierQuantiteActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed

        PanierDAO pDAO = new PanierDAO();
        try {
            String nom = model.getValueAt(jTable1.getSelectedRow(), 0).toString();
            int id_A = pDAO.findArticleByNom(nom);
            pDAO.supprimerArticleDuPanier(nom);
            PanierModel panier = new PanierModel();
            jTable1.setModel(panier);
            panier.fireTableDataChanged();
            model = (AbstractTableModel) jTable1.getModel();
            tfQuantite.setText(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, " Veuillez selectionez un article ");

        }

    }//GEN-LAST:event_supprimerActionPerformed

