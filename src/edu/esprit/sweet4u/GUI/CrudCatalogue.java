    private void ajouterCatalogueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterCatalogueActionPerformed
        CatalogueD cDAO = new CatalogueD();
        if (tfNom.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, " Champ catalogue est vide!!!!!!!");}
        else {
            int  resultat = cDAO.trouverIdCatalogueParNom(tfNom.getText());
            if (resultat != 0) {
                JOptionPane.showMessageDialog(rootPane, "Catalogue déja existant");
            }
            else{
                String NomCatalogue = tfNom.getText();

                k.setNom(NomCatalogue);
                cDAO.ajouterCatalogue(k);
                JOptionPane.showMessageDialog(this, " catalogue ajouté");
                tablCatalogue tablcatal = new  tablCatalogue();
                jTable1.setModel( tablcatal);
                tablcatal.fireTableDataChanged();
                model=(AbstractTableModel)jTable1.getModel();
                tfNom.setText(null);
                
            }
         tfimage.setIcon(new ImageIcon(homeimg));}
    }//GEN-LAST:event_ajouterCatalogueActionPerformed

    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        // TODO add your handling code here:
        CatalogueD cDAO = new CatalogueD();

        try {
              
            String nom = model.getValueAt(jTable1.getSelectedRow(),0).toString();

            k.setNom(tfNom.getText());
            k.setLien(tfimage.getIcon().toString());
            System.out.println(k);
            int idCa=cDAO.trouverIdCatalogueParNom(nom);
            cDAO.modifierCatalogue(k,idCa);
            JOptionPane.showMessageDialog(null, "Catalogue modifié avec succés");
            tfNom.setText(null);

            tablCatalogue tablcatal = new  tablCatalogue();
            jTable1.setModel( tablcatal);
            tablcatal.fireTableDataChanged();
            model=(AbstractTableModel)jTable1.getModel();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, " Veuillez selectionez un catalogue ");
        }
    }//GEN-LAST:event_modifierActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        // TODO add your handling code here:
        CatalogueD cDAO = new CatalogueD();

        try {
            String a  =   model.getValueAt(jTable1.getSelectedRow(),0).toString();
            int idC= cDAO.trouverIdCatalogueParNom(a);
            Catalogue catalog = new Catalogue();
            Article arti=new Article();
            cDAO.supprimerCatalogue(catalog,idC);
            cDAO.supprimerArticleDuCatalogue(arti, idC);
            tablCatalogue tablcatal = new  tablCatalogue();
            jTable1.setModel( tablcatal);
            tablcatal.fireTableDataChanged();
            model=(AbstractTableModel)jTable1.getModel();
            tfNom.setText(null);
            tfimage.setIcon(new ImageIcon());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, " Veuillez selectionez un catalogue ");
        }

    }//GEN-LAST:event_supprimerActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        CatalogueD cDAO = new CatalogueD();
        tfNom.setText(String.valueOf(model.getValueAt(jTable1.getSelectedRow(),0)));

        String kar  =   model.getValueAt(jTable1.getSelectedRow(),0).toString();
        int idC= cDAO.trouverIdCatalogueParNom(kar);
        String s =  cDAO.trouverCatalogueParId(idC).getLien();

        if (!s.isEmpty() || !s.equals(null))
        tfimage.setIcon(new ImageIcon(s));
        else
        tfimage.setIcon(new ImageIcon(homeimg));
    }//GEN-LAST:event_jTable1MouseClicked

